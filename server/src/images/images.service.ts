import { Injectable } from '@nestjs/common';
import { CreateImageDto } from './dto/create-image.dto';
import { UpdateImageDto } from './dto/update-image.dto';
import { InjectRepository } from '@nestjs/typeorm';
import { Repository } from 'typeorm';
import { Image } from './entities/image.entity';

@Injectable()
export class ImagesService {
  constructor( @InjectRepository(Image) private ImageRepos: Repository<Image>) {}
  async create(createImageDto: any) {
    const {imageUrl,productId} = await createImageDto
    const image = this.ImageRepos.create({
      imageUrl:imageUrl,
      product:productId
    })
    return await this.ImageRepos.save(image)
  }

  findAll() {
    return `This action returns all images`;
  }

  findOne(id: number) {
    return `This action returns a #${id} image`;
  }

  update(id: number, updateImageDto: UpdateImageDto) {
    return `This action updates a #${id} image`;
  }

  remove(id: number) {
    return `This action removes a #${id} image`;
  }
}
